# Lista são dados com uma coleção ordenada de valores o qual é permitido conter
# diferentes tipos de dados, sendo organizados por índice, onde seus elementos
# podem ser mutáveis.

dados_imc = ['ROBSON ALVES FEIJÓ', [34, 81.55, 1.69], True, None]
print(dados_imc)
print(type(dados_imc))

# Exibindo um dado da lista:
print(dados_imc[0])

# Realizando alteração em uma lista:
dados_imc[2] = False
print(dados_imc)


