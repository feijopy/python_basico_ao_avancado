# Set são conjuntos de dados com valores únicos, caso seja adicionado elementos
# iguais, o elemento adicionado será ignorado, mas, assim como listas e tuplas,
# é permitido conter diferentes tipos de dados. Por não ser ordenado, não é
# possível acessar elementos por índice. Para acessar um elemento de um set,
# é feito a verificação de pertencimento, o qual consulta se um elemento está no
# conjunto.

dados_imc = {'ROBSON ALVES FEIJÓ', 34, 81.55, 1.69, True, None}
print(dados_imc)
print(type(dados_imc))

