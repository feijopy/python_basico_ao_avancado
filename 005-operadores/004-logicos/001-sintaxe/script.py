# Operadores lógicos são usados para verificar se determinadas condições são
# verdadeiras ou falsas. Normalmente usamos quando precisamos combinar ou
# avaliar várias condições.

# OPERADOR DE CONJUNÇÃO 'and' = "E": 
# True and True   = True
# True and False  = False
# False and True  = False
# False and False = False

# OPERADOR DE DISJUNÇÃO 'or'  = "OU":
# True or True    = True
# True or False   = True
# False or True   = True
# False or False  = False

# OPERADOR DE NEGAÇÃO 'not'  = "NÃO":
# not True        = False
# not False       = True

