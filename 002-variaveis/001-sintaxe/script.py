# São espaços alocados na memória do computador para armazenar dados em tempo de
# execução. As variáveis possuem três etapas, as quais são:
#
#  1 º DECLARAÇÃO    : Etapa em que a variável é criada na memória.
#  2 º INICIALIZAÇÃO : Etapa em que é atribuído valor a variável. 
#  3 º ATRIBUIÇÃO    : Etapa em que o valor da variável é alterado.

# ?PRINCIPAIS REGRAS:
# - São Case-sensitive, ou seja, diferencia letras maiúsculas das minúsculas.
# - Podem iniciar com letras, $, _ ou números;
# - Não pode conter espaços;
# - Não pode conter caracteres especiais;
# - Não pode conter palavras reservadas da linguagem:
# https://docs.python.org/pt-br/3/reference/lexical_analysis.html#keywords

# ?BOAS PRÁTICAS:
# - Nomear as variáveis usando o padrão snake_case, exemplos: nome_completo,
# data_nascimento.


# Declarando e inicializando a variável:
nome_completo = None
print(nome_completo)

# Atribuindo valor a variável:
nome_completo = 'ROBSON ALVES FEIJÓ'
print(nome_completo)

# Reatribuindo valor a variável:
nome_completo = 'RAQUEL COSTA LIMA'
print(nome_completo)
# !NOTA¹: Em Python você tem a possibilidade de reatribuir variáveis, por se
# !tratar de uma linguagem dinamicamente tipada, o que significa que seu tipo
# !pode ser alterado durante o tempo de execução. Além disso, atribuir valor a
# !variável é opcional, diferente das linguagens compiladas.

